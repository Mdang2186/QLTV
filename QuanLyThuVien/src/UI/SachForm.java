private void loadComboBoxData() {
    try {
         Connection conn = DatabaseConnection.getConnection();
         Statement stmt = conn.createStatement();

        // Load dữ liệu nhà xuất bản (MaNhaXuatBan và TenNhaXuatBan)
        ResultSet rsNXB = stmt.executeQuery("SELECT MaNhaXuatBan FROM nhaxuatban");
        cbNXB_DCM.removeAllItems(); // Xoá các item cũ
        while (rsNXB.next()) {
            String maNXB = rsNXB.getString("MaNhaXuatBan");
            cbNXB_DCM.addItem(maNXB);
        }

        // Load dữ liệu tác giả (MaTacGia và TenTacGia)
        ResultSet rsTG = stmt.executeQuery("SELECT MaTacGia FROM tacgia");
        cbTG_DCM.removeAllItems();
        while (rsTG.next()) {
            String maTacGia = rsTG.getString("MaTacGia");
            cbTG_DCM.addItem(maTacGia);
        }

        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi load dữ liệu combobox: " + e.getMessage());
    }
}



    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try (Connection conn = DatabaseConnection.getConnection()) {
        String sql = "INSERT INTO sach (MaSach, TenSach, LoaiSach, MaTacGia, MaNhaXuatBan, NgayXuatBan, SoLuong) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, mS_DCM.getText());
        pst.setString(2, tS_DCM.getText());
        pst.setString(3, lS_DCM.getText());
        pst.setString(4, cbTG_DCM.getSelectedItem().toString());
        pst.setString(5, cbNXB_DCM.getSelectedItem().toString());
        pst.setString(6, ngayXB_DCM.getText());
        pst.setInt(7, Integer.parseInt(sL_DCM.getText()));

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(null, "Thêm sách thành công!");
        }
        loadData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Lỗi khi thêm sách: " + e.getMessage());
    } // TODO add your handling code here:
    }//GEN-LAST:event_btnThemActionPerformed


