private void loadData() {
    try {
        // Sử dụng DatabaseConnection để kết nối
        Connection conn = DatabaseConnection.getConnection();
        String sql = "SELECT * FROM sach";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        // Chuẩn bị model cho bảng
        DefaultTableModel model = (DefaultTableModel) tbS.getModel();
        model.setRowCount(0);  // Xóa dữ liệu trong bảng

        while (rs.next()) {
            String maSach = rs.getString("MaSach");
            String tenSach = rs.getString("TenSach");
            String loaiSach = rs.getString("LoaiSach");
            String maTacGia = rs.getString("MaTacGia");
            String maNXB = rs.getString("MaNhaXuatBan");
            String ngayXuatBan = rs.getString("NgayXuatBan");
            int soLuong = rs.getInt("SoLuong");

            model.addRow(new Object[]{maSach, tenSach, loaiSach, maTacGia, maNXB, ngayXuatBan, soLuong});
        }

        // Đảm bảo đóng kết nối sau khi sử dụng
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Lỗi khi tải dữ liệu: " + e.getMessage());
    }
}

